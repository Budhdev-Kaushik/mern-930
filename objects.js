function searching(array, key, keyword) {
  return array.filter((user) =>
    // user[key.toLowerCase()].includes(keyword.toLowerCase())
    user[key]
      .split(" ")
      .some((token) => token.toUpperCase().startsWith(keyword.toUpperCase()))
  );
}

function sorting(array, key, sortOrder = 1) {
  array.sort((i, j) => {
    i = i[key].toUpperCase();
    j = j[key].toUpperCase();
    if (i === Number) {
      array.sort((a, b) => a - b);
    } else {
      if (sortOrder === 1) {
        if (i > j) return 1;
        if (i < j) return -1;
      } else {
        if (i > j) return -1;
        if (i < j) return 1;
      }
      return 0;
    }
  });

  return array;
}

 